"""## Time-Based Feature Engineering

Temporal information was transformed into meaningful features:

- Hour
- Minute
- Time Slot
- Cyclic Encoding using sine and cosine transformations

These features help capture recurring traffic patterns throughout the day.
"""

from sklearn.model_selection import KFold
import numpy as np
import pandas as pd

# ==========================================
# CYCLICAL TIME FEATURES
# ==========================================

for df in [X_train, X_test]:

    df["time_sin"] = np.sin(
        2 * np.pi * df["time_slot"] / 96
    )

    df["time_cos"] = np.cos(
        2 * np.pi * df["time_slot"] / 96
    )

    df["hour_sin"] = np.sin(
        2 * np.pi * df["hour"] / 24
    )

    df["hour_cos"] = np.cos(
        2 * np.pi * df["hour"] / 24
    )

# ==========================================
# INTERACTION FEATURES
# ==========================================

for df in [X_train, X_test]:

    df["vehicle_lane_interaction"] = (
        df["LargeVehicles"] *
        df["NumberofLanes"]
    )

    df["highway_lanes"] = (
        df["RoadType_Highway"] *
        df["NumberofLanes"]
    )

    df["street_lanes"] = (
        df["RoadType_Street"] *
        df["NumberofLanes"]
    )

    # New features

    df["is_peak"] = (
        (df["time_slot"] >= 28) &
        (df["time_slot"] <= 60)
    ).astype(int)

    df["is_night"] = (
        (df["time_slot"] >= 76) |
        (df["time_slot"] <= 8)
    ).astype(int)

    df["highway_peak"] = (
        df["RoadType_Highway"] *
        df["is_peak"]
    )

    df["vehicles_per_lane"] = (
        df["LargeVehicles"] /
        (df["NumberofLanes"] + 1)
    )

    df["lat_lon"] = (
        df["latitude"] *
        df["longitude"]
    )

# ==========================================
